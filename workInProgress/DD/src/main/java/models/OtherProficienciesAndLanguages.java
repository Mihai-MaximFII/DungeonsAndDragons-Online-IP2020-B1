package models;

public class OtherProficienciesAndLanguages {
    String Languages;
    String Armour;
    String Weapons;
    String Tools;
    String Skills;
    public OtherProficienciesAndLanguages(String Languages,
            String Armour,
            String Weapons,
            String Tools,
            String Skills){
        this.Armour=Armour;
        this.Languages = Languages;
        this.Skills = Skills;
        this.Tools = Tools;
        this.Weapons =Weapons;
    }
}
