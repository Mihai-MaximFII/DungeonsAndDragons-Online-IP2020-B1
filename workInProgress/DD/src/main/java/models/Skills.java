package models;

public class Skills {
    int acrobatics;
    int animalHandling;
    int arcana;
    int athletics;
    int deception;
    int history;
    int insight;
    int intimidation;
    int investigation;
    int medicine;
    int nature;
    int perception;
    int performance;
    int persuasion;
    int religion;
    int sleightOfHand;
    int stealth;
    int survival;
    public Skills(int acrobatics,
            int animalHandling,
            int arcana,
            int athletics,
            int deception,
            int history,
            int insight,
            int intimidation,
            int investigation,
            int medicine,
            int nature,
            int perception,
            int performance,
            int persuasion,
            int religion,
            int sleightOfHand,
            int stealth,
            int survival){
        this.acrobatics= acrobatics;
        this.arcana= arcana;
        this.animalHandling = animalHandling;
        this.athletics = athletics;
        this.deception = deception;
        this.history = history;
        this.insight = insight;
        this.intimidation = intimidation;
        this.investigation = investigation;
        this.medicine =medicine;
        this.nature =nature;
        this.perception =perception;
        this.performance = performance;
        this.persuasion = persuasion;
        this.religion=religion;
        this.sleightOfHand = sleightOfHand;
        this.stealth =stealth;
        this.survival = survival;
    }
}
